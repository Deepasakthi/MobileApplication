package com.example.deepaganesan.texoprinters;

public class OrderInfo {

    public String names;
    public String addr;
    public String model;
    public String colour;
    public String msize;
    public String ddate;
    public String nos;

    public OrderInfo(String names, String addr, String model, String colour, String msize, String ddate, String nos) {
        this.names = names;
        this.addr = addr;
        this.model = model;
        this.colour = colour;
        this.msize = msize;
        this.ddate = ddate;
        this.nos = nos;
    }
}
